const items = Turkish1eItems

// Function to shuffle an array
const shuffleArray = array => {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
};

// Function to select options and ensure the correct one is included
const selectOptions = (items, correctItem, numOptions = 4) => {
    const shuffled = shuffleArray([...items]);
    const options = shuffled.slice(0, numOptions);
    if (!options.includes(correctItem)) {
        options[Math.floor(Math.random() * numOptions)] = correctItem;
    }
    return options;
};

// Generates a single question of specified type
const generateQuestion = (item, config) => {
    const options = selectOptions(items, item.correctAnswer, 4);
    switch (config.type) {
        case 'grammar-cloze':
            return {
                qType: 'grammar-cloze',
                question: item.sentence,
                options,
                correctAnswer: item.correctAnswer,
                translated: item.translated,
                explanation: item.explanation,
                words: item.words
            };
        case 'grammar-word-order-translate':
            return {
                qType: 'grammar-word-order-translate',
                prompt: config.questionText,
                translated: item.translated,
                correctAnswerFill: item.correctAnswerFill,
                wordsFill: item.wordsFill
            };
        case 'grammar-word-order-audio':
            return {
                qType: 'grammar-word-order-audio',
                audioUri: item.media,
                question: config.questionText,
                correctAnswerFill: item.correctAnswerFill,
                wordsFill: item.wordsFill
            };
        default:
            return null;  // Return null if type is not recognized
    }
};

// Configuration array
const questionConfigs = [
    { type: 'grammar-cloze', questionText: '' }, // No specific question text needed for cloze
    { type: 'grammar-word-order-translate', questionText: 'Translate this sentence:' },
    { type: 'grammar-word-order-audio', questionText: 'Select the words you hear.' },
];

// Generate questions for all items
const generateQuiz = (items, configs) => {
    return items.flatMap(item =>
        configs.map(config => generateQuestion(item, config))
    );
};

const grammarQuizQuestions = generateQuiz(Turkish1eItems, questionConfigs);
const shuffledGrammarQuizQuestions = shuffleArray([...grammarQuizQuestions]);

